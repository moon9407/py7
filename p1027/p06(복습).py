a = 758.1234567
# 소수점 2자리까지 출력하시오.
print("%.2f"% a)

b = 25.05
#10자리 공간을 만들고 빈공간을 0으로 채워 소수점 첫째자리까지 출력하시오.
print("%010.1f"% b)

c = 150.15
# 정수, 실수 ,문자열로 출력하시오.
# int(), float(), str() 타입변경방법
print("%d"% c)
print("%f"% c)
print("%s"% c)

